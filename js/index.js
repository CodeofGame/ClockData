	function popShow(event, state) {
		if (state == "show") {
			$(event).css({
				"display": "block"
			}).animate({
				"top": "0"
			}, "easing");
		} else if (state == "hide") {
			$(event).animate({
				"top": "101%"
			}, "fast", function () {
				$(event).css({
					"display": "none"
				});
			});
		}
	}
